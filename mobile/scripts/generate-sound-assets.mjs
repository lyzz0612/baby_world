import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const soundsRoot = path.join(__dirname, '../assets/sounds');
const outFile = path.join(__dirname, '../src/data/soundAssets.ts');

function walk(dir, base = '') {
  const entries = [];
  if (!fs.existsSync(dir)) return entries;
  for (const name of fs.readdirSync(dir)) {
    const full = path.join(dir, name);
    const rel = base ? `${base}/${name}` : name;
    if (fs.statSync(full).isDirectory()) {
      entries.push(...walk(full, rel));
    } else if (/\.(mp3|ogg|wav)$/i.test(name)) {
      entries.push(rel.replace(/\\/g, '/'));
    }
  }
  return entries;
}

const files = walk(soundsRoot);
const lines = files.map((rel) => {
  const key = rel;
  const reqPath = `../../assets/sounds/${rel}`;
  return `  '${key}': require('${reqPath}'),`;
});

const content = `// AUTO-GENERATED by scripts/generate-sound-assets.mjs — do not edit by hand
import { type AudioSource } from 'expo-audio';

const SOUND_MODULES: Record<string, AudioSource> = {
${lines.join('\n')}
};

export function getSoundSource(relativePath: string): AudioSource | null {
  const key = relativePath.replace(/^\\/sounds\\//, '').replace(/^\\//, '');
  return SOUND_MODULES[key] ?? null;
}

export function getTtsSource(animalId: string): AudioSource | null {
  return SOUND_MODULES[\`tts/\${animalId}.mp3\`] ?? null;
}
`;

fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.writeFileSync(outFile, content, 'utf8');
console.log(`Wrote ${files.length} sound mappings to ${outFile}`);
